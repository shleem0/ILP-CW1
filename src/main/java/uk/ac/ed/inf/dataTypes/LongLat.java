package uk.ac.ed.inf.dataTypes;

public class LongLat {

    public double lng;
    public double lat;


    public double getLng(){
        return this.lng;
    }

    public double getLat(){
        return this.lat;
    }

    public void setLng(double lng){
        this.lng = lng;
    }

    public void setLat(double lat){
        this.lat = lat;
    }
}
