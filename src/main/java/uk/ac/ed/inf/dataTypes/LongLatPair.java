package uk.ac.ed.inf.dataTypes;

public class LongLatPair{

    private LongLat position1;
    private LongLat position2;

    public LongLatPair(LongLat position1, LongLat position2){
        this.position1 = position1;
        this.position2 = position2;
    }

    public LongLat getPos1(){
        return position1;
    }

    public LongLat getPos2(){
        return position1;
    }

}
